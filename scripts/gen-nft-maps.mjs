/**
 * Generates lib/nftMaps.ts from the source image folders and assignments CSV.
 * Run once: node scripts/gen-nft-maps.mjs
 */
import { readFileSync, writeFileSync, readdirSync, statSync } from 'fs';
import { join } from 'path';

// ─── Founder Pass ────────────────────────────────────────────────────────────
const refRareSize = statSync('imgs/naka_labs/12_month.png').size;
const fpRareIds = readdirSync('imgs/naka_labs/output')
  .filter(f => statSync(join('imgs/naka_labs/output', f)).size === refRareSize)
  .map(f => parseInt(f))
  .sort((a, b) => a - b);

// ─── NIPPO ───────────────────────────────────────────────────────────────────
const csv = readFileSync('imgs/nippo_archives/assignments.csv', 'utf-8');
const nippoRows = csv.trim().split('\n').slice(1).map(line => {
  const [tokenId, name, , lineage, rarity, sourceFile] = line.split(',');
  return { id: parseInt(tokenId), name: name.trim(), lineage: lineage.trim(), rarity: rarity.trim(), sourceFile: sourceFile.trim() };
});

// ─── Emit TypeScript ─────────────────────────────────────────────────────────
const fpRareSet = `new Set<number>([\n  ${fpRareIds.join(', ')},\n])`;

const nippoEntries = nippoRows
  .map(r => `  ${r.id}: { name: ${JSON.stringify(r.name)}, lineage: ${JSON.stringify(r.lineage)}, rarity: ${JSON.stringify(r.rarity)}, img: '/nfts/nippo/${r.sourceFile}' }`)
  .join(',\n');

const out = `// AUTO-GENERATED by scripts/gen-nft-maps.mjs — do not edit by hand

// ─── Founder Pass ─────────────────────────────────────────────────────────────

export type FPTier = '6-month' | '12-month';

const FP_RARE_IDS = ${fpRareSet};

export function fpTier(tokenId: number): FPTier {
  return FP_RARE_IDS.has(tokenId) ? '12-month' : '6-month';
}

export function fpImg(tokenId: number): string {
  return FP_RARE_IDS.has(tokenId) ? '/nfts/fp-12month.jpg' : '/nfts/fp-6month.jpg';
}

// ─── NIPPO Pedigree Archives ───────────────────────────────────────────────────

export type NippoRarity = 'NAKAMANIAC' | 'NAKALUNATIC';

export interface NippoMeta {
  name: string;
  lineage: string;
  rarity: NippoRarity;
  img: string;
}

export const NIPPO_MAP: Record<number, NippoMeta> = {
${nippoEntries},
};
`;

writeFileSync('lib/nftMaps.ts', out);
console.log(`Written lib/nftMaps.ts`);
console.log(`Founder Pass rare IDs (${fpRareIds.length}): ${fpRareIds.join(', ')}`);
console.log(`NIPPO entries: ${nippoRows.length}`);
